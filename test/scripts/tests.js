function defaultFunc() {
  document.getElementById('results').innerHTML = 'This is the default!';
}

function mouseOver() {
  document.getElementById('results').innerHTML = 'This was called on mouseover!';
}

function mouseDown() {
  document.getElementById('results').innerHTML = 'This was called on mousedown!';
}

function mouseUp() {
  document.getElementById('results').innerHTML = 'This was called on mouseup!';
}
